function ProgressRing({ pct, color, size = 52 }) {
  const r = (size - 6) / 2;
  const circ = 2 * Math.PI * r;
  const dash = (pct / 100) * circ;
  return (
    <svg width={size} height={size} style={{ transform: "rotate(-90deg)" }}>
      <circle cx={size/2} cy={size/2} r={r} fill="none" stroke="rgba(255,255,255,0.07)" strokeWidth={5}/>
      <circle cx={size/2} cy={size/2} r={r} fill="none" stroke={color} strokeWidth={5}
        strokeDasharray={`${dash} ${circ}`} strokeLinecap="round"
        style={{ transition: "stroke-dasharray 0.5s ease" }}/>
    </svg>
  );
}

function InlineMd({ text }) {
  const parts = text.split(/(\*\*.*?\*\*|`[^`]+`)/g);
  return <>{parts.map((p, i) => {
    if (p.startsWith("**") && p.endsWith("**")) return <strong key={i} style={{ color: "#e0ecff" }}>{p.slice(2,-2)}</strong>;
    if (p.startsWith("`") && p.endsWith("`")) return <code key={i} style={{ background:"rgba(255,255,255,0.1)", padding:"1px 5px", borderRadius:4, fontSize:12, fontFamily:"monospace", color:"#7dd3fc" }}>{p.slice(1,-1)}</code>;
    return <span key={i}>{p}</span>;
  })}</>;
}

function RichText({ text }) {
  const lines = text.split("\n");
  const els = [];
  let tableLines = [];
  let inTable = false;

  const flushTable = () => {
    if (tableLines.length < 2) { tableLines=[]; inTable=false; return; }
    const heads = tableLines[0].split("|").map(h=>h.trim()).filter(Boolean);
    const rows = tableLines.slice(2).map(r=>r.split("|").map(c=>c.trim()).filter(Boolean));
    els.push(
      <div key={els.length} style={{ overflowX:"auto", margin:"10px 0" }}>
        <table style={{ width:"100%", borderCollapse:"collapse", fontSize:13 }}>
          <thead>
            <tr>{heads.map((h,i)=>(
              <th key={i} style={{ padding:"7px 10px", textAlign:"left", background:"rgba(255,255,255,0.06)", borderBottom:"1px solid rgba(255,255,255,0.1)", color:"#c8d6f0", fontWeight:700 }}>
                <InlineMd text={h.replace(/\*\*/g,"")} />
              </th>
            ))}</tr>
          </thead>
          <tbody>{rows.map((row,ri)=>(
            <tr key={ri} style={{ borderBottom:"1px solid rgba(255,255,255,0.04)" }}>
              {row.map((cell,ci)=>(
                <td key={ci} style={{ padding:"7px 10px", color:"#9aacbe", verticalAlign:"top" }}>
                  <InlineMd text={cell} />
                </td>
              ))}
            </tr>
          ))}</tbody>
        </table>
      </div>
    );
    tableLines=[]; inTable=false;
  };

  for (let i=0; i<lines.length; i++) {
    const line = lines[i];
    if (line.startsWith("|")) { inTable=true; tableLines.push(line); continue; }
    if (inTable) flushTable();
    if (!line.trim()) { els.push(<div key={i} style={{height:6}}/>); continue; }
    if (line.startsWith("- ")) {
      els.push(<div key={i} style={{ display:"flex", gap:8, marginBottom:3 }}>
        <span style={{ color:"#5c9ee0", marginTop:2, flexShrink:0, fontSize:12 }}>▸</span>
        <p style={{ margin:0, color:"#9aacbe", fontSize:13.5, lineHeight:1.75 }}><InlineMd text={line.slice(2)}/></p>
      </div>);
    } else if (/^\d+\./.test(line)) {
      const [num, ...rest] = line.split(". ");
      els.push(<div key={i} style={{ display:"flex", gap:10, marginBottom:3 }}>
        <span style={{ color:"#9b5ce0", fontWeight:700, minWidth:18, fontSize:13 }}>{num}.</span>
        <p style={{ margin:0, color:"#9aacbe", fontSize:13.5, lineHeight:1.75 }}><InlineMd text={rest.join(". ")}/></p>
      </div>);
    } else {
      els.push(<p key={i} style={{ margin:"3px 0", color:"#9aacbe", fontSize:13.5, lineHeight:1.8 }}><InlineMd text={line}/></p>);
    }
  }
  if (inTable) flushTable();
  return <div>{els}</div>;
}

